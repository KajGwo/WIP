import math 

# A program to calculate the volume and surface area of ​​a cube.
cubeside = float(input("cube side? "))
cubevolume = cubeside ** 3
cubesurface = (cubeside ** 2) * 6
print(f'The volume of a cube with side {cubeside} is {cubevolume}')
print(f'The surface area of a cube with side {cubeside} is {cubesurface}')









